
public class RatNumber 
	
	{
	    int num;            // The numerator
	    int den;            // The denominator
	    
	    public RatNumber(int x, int y)
	    {
	        num = x;		
	        den = y;		
	    }

	    public String toString()
	    {
	        return "(" + num + "/" + den + ")";
	    }
	}


